/** \brief Suma dos numeros.
 *
 * \param Recibe un primer numero a sumar(A).
 * \param Recibe un segundo numero a sumar(B).
 * \return Devuelve la suma entre A y B.
 *
 */

int Sumar(int A, int B);

/** \brief Resta dos numeros.
 *
 * \param Recibe un primer numero a restar(A).
 * \param Recibe un segundo numero a restar (B).
 * \return Devuelve la resta entre A y B.
 *
 */
int Restar(int A, int B);

/** \brief Multiplica dos numeros.
 *
 * \param Recibe un primer numero a multiplicar(A).
 * \param Recibe un segundo numero a multiplicar(B).
 * \return Devuelve la multiplicación entre A y B.
 *
 */
int Multiplicar(int A, int B);

/** \brief Divide dos numeros.
 *
 * \param Recibe un dividendo(A).
 * \param Recibe un divisor distinto de 0(B).
 * \return Devuelve la division entre A y B.
 *
 */
float Dividir(int A, int B);

/** \brief Realiza el factorial de un numero.
 *
 * \param Recibe el numero.
 *
 * \return Devuelve el factorial de dicho numero.
 *
 */
long long int Factorial(int num);
